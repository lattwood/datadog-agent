package utils

import "math/rand"

var letters = []rune("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ")

// RandomID generates a random ID of a fixed size
func RandomID(n int) string {
	b := make([]rune, n)
	for i := range b {
		b[i] = letters[rand.Intn(len(letters))]
	}
	return string(b)
}

// RandomCookie generates a random cookie for a new process cache entry
func RandomCookie() uint32 {
	return rand.Uint32()
}
